Extended_Add	PROC USES esi edi ebx ecx
;
; Description:	Calculates the sum of two extended integers stored as
;				arrays of bytes.
; Receives:		ESI = the offset of first integer
;				EDI = the offset of second integer
;				EBX = the offset of the variable which will hold the sum
;				ECX = number of bytes to be added
; Preconds:		The length of the sum variable must be one byte longer
;				than the input operands.
; Returns:		N/A
; ¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤
				clc
L1:				mov al,[esi]
				adc al,[edi]
				mov [ebx],al
				inc esi
				inc edi
				inc ebx
				loop L1
				mov BYTE PTR [ebx],0
				adc BYTE PTR [ebx],0
				ret
Extended_add	ENDP

