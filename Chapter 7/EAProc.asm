				add esi,ecx
				sub esi,TYPE BYTE
				mov ebx,TYPE BYTE
L1:				mov al,[esi]
				call WriteHexB
				sub esi,TYPE BYTE
				loop L1
				ret
Display_Sum		ENDP
				END main