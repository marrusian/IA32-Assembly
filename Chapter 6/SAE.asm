includelib	D:\masm32\lib\Irvine32.lib
				.386
				.MODEL flat,stdcall
				.STACK 100h
Crlf			PROTO				
ReadInt			PROTO
WriteInt		PROTO
WriteString		PROTO
ExitProcess		PROTO dwExitCode:DWORD
; ¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤
				.DATA
array			SDWORD 10,60,20,33,72,89,45,65,72,18

promptValue		BYTE "Insert the compare value: ",0
; ¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤¤
				.CODE
main			PROC
				mov edx,OFFSET promptValue
				call WriteString
				call ReadInt
				mov edx,eax
				mov eax,0
				mov ebx,0
				mov ecx,LENGTHOF array
				.WHILE ebx < ecx
				.IF array[ebx*TYPE DWORD] <= edx
				add eax,array[ebx*TYPE DWORD]
				.ENDIF	
				inc ebx
				.ENDW
				call WriteInt
				call Crlf
				INVOKE ExitProcess,0
main			ENDP

				END main